#!/usr/bin/env bash

echo "Taking screenshot..."

# ----------------------------------------------------- 
# Take a screenshot
# ----------------------------------------------------- 

grim -g "$(slurp)" - | swappy -f -

# ----------------------------------------------------- 
# Wait for 1 sec
# ----------------------------------------------------- 
sleep 1

# ----------------------------------------------------- 
# Send notification
# ----------------------------------------------------- 
notify-send "Screnshot taken!"

echo "Done."
